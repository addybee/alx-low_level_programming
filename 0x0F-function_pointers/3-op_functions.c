#include "3-calc.h"
/**
 * op_add - return sum of a and b
 * @a: first number
 * @b: second number
 * Return: sum of a and b
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - return difference of a and b
 * @a: first number
 * @b: second number
 * Return: difference of a and b
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - return product of a and b
 * @a: first number
 * @b: second number
 * Return: product of a and b
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - return result of division a by b
 * @a: first number
 * @b: second number
 * Return: result of division a by b
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - return remainder of division a by b
 * @a: first number
 * @b: second number
 * Return: remainder of division a by b
 */
int op_mod(int a, int b)
{
	return (a % b);
}
